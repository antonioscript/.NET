//Math
using System;

class Hello
{
    static void Main()
    {
        //Valor Máximo- > Math.Max
        Console.WriteLine(Math.Max(5,10));
        
        //Valor Mínimo- > Math.Min
        Console.WriteLine(Math.Min(5,10));
        
        //Raiz Quadrada- > Math.Sqrt
        Console.WriteLine(Math.Sqrt(64));
        
        //Valor Absoluto- > Math.Abs
        Console.WriteLine(Math.Abs(-4));
        
        //Arredondar para o maior -> Math.Round
        Console.WriteLine(Math.Round(9.99)); // out = 10
    }
}